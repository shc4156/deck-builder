// 텍스트 매칭용 평탄화 맵: { 용어: { definition, category } }
export const GLOSSARY_FLAT = Object.entries(GLOSSARY_CATEGORIES).reduce(
  (acc, [category, terms]) => {
    Object.entries(terms).forEach(([term, definition]) => {
      acc[term] = { definition, category };
    });
    return acc;
  },
  {}
);